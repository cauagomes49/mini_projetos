class Espiral:
    def __init__(self):
        self.nome = ' - '
        self.preco = 0
        self.quantidade = 0

    def getNomeDoProduto(self):
        return self.nome

    def setNomeDoProduto(self, nome):
        self.nome = nome

    def getPreco(self):
        return self.preco
    def setPreco(self, preco):
        self.preco = preco

    def getQuantidade(self):
        return self.quantidade

    def setQuantidade(self,quantidade):
        self.quantidade = quantidade

